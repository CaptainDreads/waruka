# Colour-by-residual thresholds for sideline clicks (in metres):
GOOD_M = 1.0    # < this -> green
WARN_M = 5.0    # < this -> yellow; >= -> red (also = "BAD" for R / save print)

# BGR colours
CLR_GOOD = (0, 220, 0)
CLR_WARN = (0, 220, 220)
