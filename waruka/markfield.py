"""Interactive field-marking tool (OpenCV HighGUI).

    python -m waruka markfield input_video_short.mp4 --time 2.0

Mark the four field corners on the RAW panorama (it shows the whole field;
the calibrated ray model handles the de-warp mathematically). Then add
extra points along each sideline -- they pin the cross-field Z scale that
4 corners alone cannot constrain.

Corner order (prompted on screen), 'left/right' as seen from the camera:
    1 NEAR-LEFT   2 NEAR-RIGHT   3 FAR-RIGHT   4 FAR-LEFT
(near = the sideline closest to the camera).

After 4 corners are placed the tool builds a fast 4-corner DLT "validator"
homography and uses it to reproject every subsequent sideline click in real
time. Dots are coloured by how far the click sits from its target line:
    green  : |Z - target| < 1 m   (good)
    yellow :              < 5 m   (borderline)
    red    :             >= 5 m   (likely wrong line / wrong field)

Predicted near (blue) and far (magenta) sideline guide lines are also drawn
once corners are placed -- click near the matching coloured line.

Once the 4th corner is placed the tool drops into AUTO mode: every
subsequent click is auto-classified as near or far by projecting it
through the 4-corner DLT and picking whichever sideline (Z=0 vs Z=W)
the predicted ground Z is closer to. Press A or B to force near or far,
or T to return to AUTO. In AUTO mode right-click removes the nearest
mark across all groups (no need to switch to corner mode to fix a
corner).

Press F to fit. The fit minimises mark error in metres (corners + sideline
points). By default it applies:
  * MLE auto-balance: per-mark weight = 1/(local click-error amplification),
    so middle marks count more than extreme-longitude ones. Disable with
    --no-auto-balance.
  * near_trust = 3.0: the near sideline (closest to the camera, easiest to
    verify visually) is weighted 3x as much as the far sideline. Tune with
    --near-trust FLOAT.
Both settings persist in project.json.
"""

