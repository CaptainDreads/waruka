# Hit-test radius (display pixels) for click-near-mark detection used by
# both drag and right-click-remove.
HIT_PX = 25

# How long after a programmatic SetCursorPos to swallow incoming
# MOUSEMOVE events (the synthetic one generated by the OS arrives
